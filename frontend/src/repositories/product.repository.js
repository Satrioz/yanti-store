  async function _fetchFromAPI(endpoint) {
    const res = await fetch(`${AppConfig.API_BASE_URL}${endpoint}`);
    if (!res.ok) throw new Error(`API error ${res.status}: ${endpoint}`);
    const json = await res.json();
    if (!json.success) throw new Error(json.error || "Unknown API error");
    return json.data;
  }

  async function getAll() {
    if (AppConfig.USE_MOCK_DATA) return Promise.resolve([..._mockProducts]);
    const raw = await _fetchFromAPI("/products");
    return raw.map(createProduct);
  }

  async function getById(id) {
    if (AppConfig.USE_MOCK_DATA)
      return Promise.resolve(_mockProducts.find((p) => p.id === id) ?? null);
    const raw = await _fetchFromAPI(`/products/${id}`);
    return createProduct(raw);
  }

  return Object.freeze({ getAll, getById });
